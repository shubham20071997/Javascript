
// Arithmetic Operators

let x = 5;
let y = 8;

console.log (x + y);      //addition
console.log (x - y);      //substraction
console.log (x * y);      //multiplication
console.log (x / y);      //division
console.log (x % y);      //remainder
console.log (x ** y);     //exponent

// // increment operator (++)
console.log (++x);           // first increment then print
console.log (x++);           //first print then increment
console.log (x) 

// // decrement operator (--)
console.log (--x);           // first decrement then print
console.log (x--);           //first print then decrement
console.log (x) 